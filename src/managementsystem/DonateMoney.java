    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        String gender = "";
        int selectedIndex = ageBox.getSelectedIndex();
        if (ageBox.getSelectedItem().equals("Male")) {
            gender = "male";
        } else if (ageBox.getSelectedItem().equals("Female")) {
            gender = "female";
        } else {
            gender = "transgender";
        }
        if (nameField.getText().trim().isEmpty() || ageBox.getSelectedIndex() == 0 || genderBox.getSelectedIndex() == 0
                || cardNumberField.getText().trim().isEmpty() || csvField.getText().trim().isEmpty() || amountField.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill all the fields!");
        } else {

            myDonor.add(new CharityForm((nameField.getText().trim()), gender, "N/A", (Integer.parseInt((String) ageBox.getItemAt(selectedIndex))),
                    (cardNumberField.getText().trim()), (Integer.parseInt(csvField.getText().trim())), (amountField.getText().trim())));

            JOptionPane.showMessageDialog(null, "Donation was successfull!");
            this.dispose();
        }
    }//GEN-LAST:event_submitButtonActionPerformed

