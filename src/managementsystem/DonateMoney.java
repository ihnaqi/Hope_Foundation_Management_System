    public DonateMoney(String user) {
        initComponents();
        populateArrayList();
        this.currentUser = user;
        currentUserField.setText(currentUser);
        for (int i = 0; i < myUser.size(); i++) {
            if (myUser.get(i).getUserName().equals(currentUser)) {
                nameField.setText(myUser.get(i).getFirstName() + " " + myUser.get(i).getLastName());
                if (myUser.get(i).getGender().equalsIgnoreCase("Male")) {
                    genderBox.setSelectedIndex(2);
                    break;
                } else if (myUser.get(i).getGender().equalsIgnoreCase("female")) {
                    genderBox.setSelectedIndex(1);
                    break;
                } else if (myUser.get(i).getGender().equalsIgnoreCase("transgender")) {
                    genderBox.setSelectedIndex(3);
                    break;
                }
            }
        }
        ageBox.setModel(new javax.swing.DefaultComboBoxModel(ageArray));
    }

    public void populateArrayList() {
        try {
            FileInputStream file = new FileInputStream("User.txt");
            ObjectInputStream inputFile = new ObjectInputStream(file);
            boolean endOfFile = false;

            while (!endOfFile) {
                try {
                    myUser.add((User) inputFile.readObject());
                } catch (EOFException e) {
                    endOfFile = true;
                } catch (Exception f) {
                    JOptionPane.showMessageDialog(null, f.getMessage());
                }
            }
            inputFile.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    public void saveUserToFile() {
        try {
            FileOutputStream file = new FileOutputStream("DonatedMoneyLis.txt");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);

            for (int i = 0; i < myDonor.size(); i++) {
                outputFile.writeObject(myDonor.get(i));
            }
            outputFile.close();
            JOptionPane.showMessageDialog(null, "Donation Successfull!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

